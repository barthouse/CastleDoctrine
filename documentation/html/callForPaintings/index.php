<h3>Beginner Tips</h3>
Some of you may have no experience making pixel art or even digital images in general.  Here are some tips.
<br>
<br>
Free tools include <a href="http://mtpaint.sf.net">mtPaint</a> (Linux and Windows) and <a href="http://www.gimp.org">GIMP</a> (Linux, Windows, and Mac).  You can also use PhotoShop if you have it, or even the Paint program that ships with Windows.<br>
<br>
Create a new 32 x 32 image, and zoom into it so that it almost fills the screen.  Pixels will become huge squares at that zoom level, and you can place them one-by-one with the mouse. 



<h3>Details</h3>
No transparency, please.  The background that the paintings will be displayed on is pure black, if that matters to you.  Your title and by line together should not be more than 44 letters long.
